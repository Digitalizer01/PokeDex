package com.pokedex.dto;

public class PokemonTypeDTO {
	int id;
	String name;

	public PokemonTypeDTO() {

	}

	public PokemonTypeDTO(String name) {
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		PokemonTypeDTO other = (PokemonTypeDTO) obj;
		if (id != other.id)
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "PokemonTypeDTO [id=" + id + ", name=" + name + "]";
	}

}
